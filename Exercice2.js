/**
 *fonction pour calculer la somme de deux nombres
 *
 * @param {number} a la première variable
 * @param {number} b la deuxième variable
 * @return {number} La somme des deux nombres.
 */
function somme(a,b){
    return a+b;
}
// Test de la fonction avec différentes valeurs
console.log(somme(5, 3));    
console.log(somme(-2, 7));   
console.log(somme(0, 0));    
